from abc import ABC, abstractmethod


class Restaurant(ABC):
    
    @abstractmethod
    def setName(self):
        pass
    
    @abstractmethod
    def setDescription(self):
        pass
    
    @abstractmethod
    def setLocation(self):
        pass
    
    @abstractmethod
    def setImage(self):
        pass
    
    @abstractmethod
    def setSize(self):
        pass
    
    @abstractmethod
    def setRating(self):
        pass
    
    @abstractmethod
    def setTel(self):
        pass
    
    @abstractmethod
    def setPrice(self):
        pass
    
    @abstractmethod
    def setCuisine(self):
        pass
    
    @abstractmethod
    def getName(self):
        pass
    
    @abstractmethod
    def getDescription(self):
        pass
    
    @abstractmethod
    def getLocation(self):
        pass
    
    @abstractmethod
    def getImage(self):
        pass
    
    @abstractmethod
    def getSize(self):
        pass
    
    @abstractmethod
    def getRating(self):
        pass
    
    @abstractmethod
    def getTel(self):
        pass
    
    @abstractmethod
    def getPrice(self):
        pass
    
    @abstractmethod
    def getCuisine(self):
        pass
    
    @abstractmethod
    def generateRestaurantID(self):
        pass
    

    
    
    